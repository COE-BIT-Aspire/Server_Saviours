// Print all the numbers from 1 to 10 using a for loop.

var num;
for(num = 1; num < 11; num += 1){
    console.log(num);
}

// Print numbers from 1 to 10 but increment by 2 in each step using a for loop.

var num2;
for(num2 = 1; num2 < 11; num2 += 2){
    console.log(num2);
}

// Print numbers from 1 to 10 in reverse order.

var numr;
for(numr = 10; numr > 0; numr -=1){
    console.log(numr);
}

// Print only the even number from 1 to 10.

var nume;
